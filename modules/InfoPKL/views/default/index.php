<h3>Sasaran Kuliah Praktek Kerja Lapangan</h3>
<p style="text-align:justify;">Praktek Kerja Lapangan diharapkan dapat dilakukan di institusi atau instansi atau perusahaan yang mendayagunakan teknologi yang diajarkan di STT-NF, sehingga mahasiswa dapat memahami prosedur kerja dan menganalisis permasalahan sehingga kemudian dapat merancang sistem dan aplikasi yang sesuai.</p>
<div id="syarat" style="text-align:justify;">
<hr />
</div>
<h3>Persyaratan</h3>
<p>Mahasiswa yang akan mengambil mata kuliah Praktek Kerja Lapangan (PKL) telah memenuhi persyaratan baik persyaratan akademik maupun persyaratan administratif.</p>
<ol>
<li>Persyaratan Akademik<br />
Mahasiswa yang telah mengambil mata kuliah sebanyak 80 sks berdasarkan <a href="https://akademiksttnf.wordpress.com/khs-kartu-hasil-studi/">KHS resmi y</a>ang dikeluarkan oleh BAAK.</li>
<li>Persyaratan Administratif<br />
Mencantumkan mata kuliah PKL dalam Kartu Rencana Studi (KRS) sebagai salah satu mata kuliah yang akan diambilnya.</li>
</ol>
